// Build context string from RAG results
export function buildContext(chunks: { content: string; metadata: Record<string, unknown> }[]): string {
  if (chunks.length === 0) return '';

  const contextParts = chunks.map((chunk, i) =>
    `[מקור ${i + 1}]: ${chunk.content}`
  );

  return `\n\nהקשר מהמסמכים הארגוניים:\n${contextParts.join('\n\n')}`;
}

// Build org profile context for the AI
export function buildOrgContext(profile: Record<string, unknown> | null, orgName?: string | null): string {
  const parts: string[] = ['\n\n===== כרטיס ארגון ====='];

  if (orgName) parts.push(`שם: ${orgName}`);

  if (!profile || Object.keys(profile).length === 0) {
    if (orgName) {
      parts.push('(הפרופיל עדיין ריק — צריך מסמכים או לינק כדי לבנות אותו)');
      return parts.join('\n');
    }
    parts.push('(אין פרופיל — הארגון חדש. תשאל שאלות כדי להכיר אותם)');
    return parts.join('\n');
  }

  if (profile.name && !orgName) parts.push(`שם: ${profile.name}`);
  if (profile.registration_number) parts.push(`ע.ר.: ${profile.registration_number}`);
  if (profile.founded_year) parts.push(`שנת הקמה: ${profile.founded_year}`);
  if (profile.mission) parts.push(`מטרה: ${profile.mission}`);
  if (profile.annual_budget) parts.push(`מחזור שנתי: ${Number(profile.annual_budget).toLocaleString('he-IL')} ש"ח`);
  if (profile.employees_count) parts.push(`עובדים: ${profile.employees_count}`);
  if (profile.beneficiaries_count) parts.push(`מוטבים: ${Number(profile.beneficiaries_count).toLocaleString('he-IL')}`);
  if (Array.isArray(profile.focus_areas)) parts.push(`תחומי פעילות: ${(profile.focus_areas as string[]).join(', ')}`);
  if (Array.isArray(profile.regions)) parts.push(`אזורים: ${(profile.regions as string[]).join(', ')}`);

  // Active projects
  if (Array.isArray(profile.active_projects) && (profile.active_projects as unknown[]).length > 0) {
    parts.push('\nפרויקטים פעילים:');
    for (const proj of profile.active_projects as { name: string; description: string; beneficiaries?: number }[]) {
      parts.push(`- ${proj.name}: ${proj.description}${proj.beneficiaries ? ` (${proj.beneficiaries} מוטבים)` : ''}`);
    }
  }

  // Key achievements
  if (Array.isArray(profile.key_achievements) && (profile.key_achievements as string[]).length > 0) {
    parts.push('\nהישגים מרכזיים:');
    for (const ach of profile.key_achievements as string[]) {
      parts.push(`- ${ach}`);
    }
  }

  // Summary
  if (profile.summary) parts.push(`\nסיכום: ${profile.summary}`);

  return parts.join('\n');
}
